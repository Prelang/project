require "active_support/all"
require "git"

# ================================================
# MODULE->PROJECT ================================
# ================================================
module Project

  # ----------------------------------------------
  # ATTRIBUTES -----------------------------------
  # ----------------------------------------------
  mattr_accessor :commands, :configuration

  commands = []

  # ----------------------------------------------
  # COMMAND --------------------------------------
  # ----------------------------------------------
  def self.scripts
    Dir.glob "project/*"
  end

  # ----------------------------------------------
  # USAGE ----------------------------------------
  # ----------------------------------------------
  def self.usage
    puts "usage: project <action> [--dry-run]"
    return 1
  end

  # ----------------------------------------------
  # MESSAGING ------------------------------------
  # ----------------------------------------------
  def self.fatal(message, error_code=1)
    puts "fatal: #{message}"
    exit error_code
  end

  # ----------------------------------------------
  # MAIN -----------------------------------------
  # ----------------------------------------------
  def self.main

    unless File.exist? "Projectfile"
      fatal "Projectfile not found."
    end




    # Successful exit
    exit 0
  end
  
end

